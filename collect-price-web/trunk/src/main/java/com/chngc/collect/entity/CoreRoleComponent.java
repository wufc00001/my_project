package com.chngc.collect.entity;

import java.io.Serializable;

/**
 * Created by wufc on 2019/8/24.
 */
public class CoreRoleComponent implements Serializable{

    private String roleComponentId;

    private String roleId;

    private String componentId;

    private String componentCode;

    public String getRoleComponentId() {
        return roleComponentId;
    }

    public void setRoleComponentId(String roleComponentId) {
        this.roleComponentId = roleComponentId;
    }

    public String getRoleId() {
        return roleId;
    }

    public void setRoleId(String roleId) {
        this.roleId = roleId;
    }

    public String getComponentId() {
        return componentId;
    }

    public void setComponentId(String componentId) {
        this.componentId = componentId;
    }

    public String getComponentCode() {
        return componentCode;
    }

    public void setComponentCode(String componentCode) {
        this.componentCode = componentCode;
    }
}
